/*
 * Créez un mini-printf: my_printf(const char *format, ...)
 * 
 * Supportez:
 * - %d : int
 * - %s : char*
 * - %c : char
 * - %% : caractère %
 * 
 * Exemple:
 * my_printf("Hello %s, you have %d messages\n", "Alice", 5);
 * → Hello Alice, you have 5 messages
 * 
 * Bonus: supportez %x (hexadécimal) et %p (pointeur)
 */